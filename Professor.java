
public class Professor {

	private String OriginatedProfessorName;

	private String departmentName;
	
	private String SchoolName;
	
	public String getOriginatedProfessorName() {
		return OriginatedProfessorName;
	}

	public void setOriginatedProfessorName(String originatedProfessorName) {
		OriginatedProfessorName = originatedProfessorName;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public String getSchoolName() {
		return SchoolName;
	}

	public void setSchoolName(String schoolName) {
		SchoolName = schoolName;
	}

	
}
